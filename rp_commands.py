def format_rp_action(action_key, user, target):
    """
    Форматирует RP действие
    
    Args:
        action_key: ключ действия из RP_COMMANDS
        user: имя пользователя, выполняющего действие
        target: имя целевого пользователя
    
    Returns:
        Отформатированная строка с действием
    """
    if action_key not in RP_COMMANDS:
        return None
    
    cmd = RP_COMMANDS[action_key]
    text = cmd['text'].format(user=user, target=target)
    emoji = cmd['emoji']
    
    return f"{emoji} {text}"

def get_rp_reply(action_key):
    """
    Получает ответ на RP действие
    
    Args:
        action_key: ключ действия из RP_COMMANDS
    
    Returns:
        Ответный текст
    """
    if action_key not in RP_COMMANDS:
        return "Ты что делаешь?!"
    
    return RP_COMMANDS[action_key]['reply']

def get_all_rp_commands():
    """Получить все доступные RP команды"""
    return list(RP_COMMANDS.keys())

