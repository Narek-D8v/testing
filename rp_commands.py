def format_rp_action(action_key, user, target):
    if action_key not in RP_COMMANDS:
        return None
    cmd = RP_COMMANDS[action_key]
    text = cmd['text'].format(user=user, target=target)
    emoji = cmd['emoji']
    return f"{emoji} {text}"

def get_rp_reply(action_key):
    if action_key not in RP_COMMANDS:
        return "Ты что делаешь?!"
    return RP_COMMANDS[action_key]['reply']

def get_all_rp_commands():
    return list(RP_COMMANDS.keys())

